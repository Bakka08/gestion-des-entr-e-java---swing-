   public void load (){
       int q;
       double p;
       int nbr;
       
    model.setRowCount(0);
    for(entree c : ss.findAll() ){
        q=0;p=0;nbr=0; 
        for(LigneDemande s : lcs.findAll()){
        if(s.getDemande().getId()==c.getDemande().getId()){
            double pq = 0;
            pq=s.getPrix()*s.getQuantite();
            nbr=nbr+1;
            q=q+s.getQuantite();
            p=p+pq;
            
        }
        
        }
        model.addRow(new Object[]{
            c.getDemande().getId(),
            c.getDemande().getFournisseur().getNom(),
            nbr,
            q,
            p,
            c.getDemande().getDate(),
                c.getId()
            
                
            
            
                
        });
    }
}
