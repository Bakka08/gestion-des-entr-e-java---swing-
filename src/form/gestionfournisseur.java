    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String nom = txtnom.getText();
          String telephone = txttelephone.getText();
           String email = txtemail.getText();
        
        if(fs.create(new Fournisseur(nom, telephone, email))){
            JOptionPane.showMessageDialog(this, "bien ajoute");
            load();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if(id!=0){
            int reponse =JOptionPane.showConfirmDialog(this, "voulez  vous vraiment supprimer cette produit");
            if (reponse==0){
                fs.delete(fs.findById(id));
                load();
                JOptionPane.showMessageDialog(this, "Bien supprim√©");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String nom = txtnom.getText();
          String telephone = txttelephone.getText();
           String email = txtemail.getText();
        if(fs.update(new Fournisseur(id, nom, telephone, email))){
                JOptionPane.showMessageDialog(this, "bien modifie");
                load();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

