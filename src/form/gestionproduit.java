public void load (){
    model.setRowCount(0);
    for(Produit p : ps.findAll() ){
        model.addRow(new Object[]{
            p.getId(),
            p.getDesignation(),
            p.getPrixAchat(),
            p.getFournisseur().getId()
        });
    }
}
 private void loadfournisseur() {
        for (Fournisseur s : fs.findAll()) {
            listfournisseur.addItem(s.getId());
        }
    }
