    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String designation = txtdesignation.getText();
        Double prixachat = Double.parseDouble(txtprixachat.getText());
        int c = (int) listfournisseur.getSelectedItem();
        if(ps.create(new Produit(designation, prixachat,fs.findById(c)))){
            JOptionPane.showMessageDialog(this, "bien ajoute");
            load();
           
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void ListproduitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListproduitMouseClicked
        id = Integer.parseInt(model.getValueAt(Listproduit.getSelectedRow(), 0).toString());
        txtdesignation.setText(model.getValueAt(Listproduit.getSelectedRow(), 1).toString());
         txtprixachat.setText(model.getValueAt(Listproduit.getSelectedRow(), 2).toString());
         listfournisseur.setSelectedItem((int)model.getValueAt(Listproduit.getSelectedRow(), 3));
         
    }//GEN-LAST:event_ListproduitMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(id!=0){
            int reponse =JOptionPane.showConfirmDialog(this, "voulez  vous vraiment supprimer cette produit");
            if (reponse==0){
                ps.delete(ps.findById(id));
                load();
                JOptionPane.showMessageDialog(this, "Bien supprimé");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         String designation = txtdesignation.getText();
        Double prixachat = Double.parseDouble(txtprixachat.getText());
        int c = (int) listfournisseur.getSelectedItem();
        if(ps.update(new Produit(id, designation, prixachat,fs.findById(c)))){
                JOptionPane.showMessageDialog(this, "bien modifie");
                load();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

